    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
         String searchRegNo = searchField.getText();
    
    // Check if searchField is null or empty
    if (searchField == null || searchRegNo.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a registration number.");
        return;
    }
    
    try (Connection con = DBconnection.connect()) {
        if (con == null) {
            JOptionPane.showMessageDialog(this, "Database connection failed!");
            return;
        }
        
        String query = "SELECT * FROM register WHERE registrationNo = ?";
        try (PreparedStatement pst = con.prepareStatement(query)) {
            pst.setString(1, searchRegNo);
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                // Populate fields with data from database
                nameField.setText(rs.getString("name"));
                phoneField.setText(rs.getString("phoneNo"));
                periodField.setText(rs.getString("period"));
                addressField.setText(rs.getString("address"));
                
                enableFields(true);
            } else {
                JOptionPane.showMessageDialog(this, "No registration found with this number!");
                clearFields();
                enableFields(false);
            }
        }
    } catch (SQLException ex) {
        Logger.getLogger(renew.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Error fetching data: " + ex.getMessage());
    }
    }//GEN-LAST:event_searchButtonActionPerformed

