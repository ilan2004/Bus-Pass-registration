    private void renewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_renewButtonActionPerformed
        // TODO add your handling code here:
       try (Connection con = DBconnection.connect()) {
        if (con == null) {
            JOptionPane.showMessageDialog(this, "Database connection failed!");
            return;
        }
        
        String query = "UPDATE register SET name=?, phoneNo=?, period=?, address=? WHERE registrationNo=?";
        try (PreparedStatement pst = con.prepareStatement(query)) {
            pst.setString(1, nameField.getText());
            pst.setString(2, phoneField.getText());
            pst.setString(3, periodField.getText());
            pst.setString(4, addressField.getText());
            pst.setString(5, searchField.getText());

            
            int result = pst.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Renewal completed successfully!");
                clearFields();
                enableFields(false);
            } else {
                JOptionPane.showMessageDialog(this, "Renewal failed!");
            }
        }
    } catch (SQLException ex) {
        Logger.getLogger(renew.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Error updating data: " + ex.getMessage());
    }
}

